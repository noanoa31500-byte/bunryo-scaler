function getIngredientAllergens(name) {
  return allergenDB.filter(a => a.keys.some(k => name.includes(k)));
}

function detectAllAllergens(ingredientNames) {
  const found = new Map(); // label → mandatory
  for (const name of ingredientNames) {
    for (const a of getIngredientAllergens(name)) {
      if (!found.has(a.label)) found.set(a.label, a.mandatory);
    }
  }
  return found; // Map<label, mandatory>
}
