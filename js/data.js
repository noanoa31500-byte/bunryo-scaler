function applyFoodAliases(name) {
  for (const [pat, rep] of FOOD_ALIASES) {
    if (pat.test(name)) return name.replace(pat, rep);
  }
  return name;
}

// 鶏肉の皮設定（'with'=皮あり, 'without'=皮なし）
var _chickenSkinPref = 'with';

// 肉類: ユーザーの入力を [species, cut] トークンに分解して複合検索
var MEAT_SPECIES = [
  [/鶏|とり肉|チキン|手羽/, 'にわとり'],
  [/豚(?!足)/,              'ぶた'],
  [/牛(?!乳|蒡)/,           'うし'],
];
var MEAT_CUTS = [
  [/もも肉|もも(?!桃)/,    'もも'],
  [/むね肉|むね/,           'むね'],
  [/ささみ/,                'ささみ'],
  [/バラ肉|ばら肉|バラ/,   'ばら'],
  [/ロース/,                'ロース'],
  [/かた肉|かた/,           'かた'],
  [/ひき肉|挽き肉/,         'ひき肉'],
  [/手羽先/,                '手羽さき'],
  [/手羽元/,                '手羽もと'],
  [/手羽/,                  '手羽'],
  [/レバー/,                'レバー'],
  [/ハツ|心臓/,             'ハツ'],
];

// foodDBEntries: [name, kcal, protein, fat, carb, sodium_mg] の配列
function pickRaw(list) {
  // 「生」のエントリ（皮つきを優先）、なければ最短名
  const raws = list.filter(e => e[0].endsWith(' 生'));
  if (raws.length > 0) {
    const skinOn = raws.find(e => e[0].includes('皮つき'));
    return skinOn || raws[0];
  }
  return list.sort((a,b) => a[0].length - b[0].length)[0];
}

function toNutrObj(e) {
  return { kcal: e[1], p: e[2], f: e[3], c: e[4], s: e[5] };
}

// qが単語境界で含まれるかチェック（「すいか」の中の「いか」を除外する）
function wordIncludes(str, q) {
  let idx = str.indexOf(q);
  while (idx !== -1) {
    const before = idx > 0 ? str[idx - 1] : '';
    if (idx === 0 || before === ' ' || before === '）' || before === '＞' || before === ']' || before === '］') return true;
    idx = str.indexOf(q, idx + 1);
  }
  return false;
}

// 皮の有無を選べる部位（ひき肉・ささみ・レバー等は皮なし固定なので除外）
var SKIN_CUTS = ['もも', 'むね', '手羽'];

function findInFoodDB(name) {
  // 肉類チェック（FOOD_ALIASESより先に判定）
  for (const [spPat, spRep] of MEAT_SPECIES) {
    if (spPat.test(name)) {
      let cut = null;
      for (const [cutPat, cutRep] of MEAT_CUTS) {
        if (cutPat.test(name)) { cut = cutRep; break; }
      }
      let pool = foodDBEntries.filter(e => e[0].includes(spRep));
      if (cut) pool = pool.filter(e => e[0].includes(cut));
      // 鶏肉の皮あり/皮なし対応（もも・むね・手羽のみ）
      if (spRep === 'にわとり' && (cut === null || SKIN_CUTS.some(sc => cut.includes(sc)))) {
        const skinKw = _chickenSkinPref === 'without' ? '皮なし' : '皮つき';
        const skinPool = pool.filter(e => e[0].includes(skinKw));
        if (skinPool.length > 0) pool = skinPool;
      }
      if (pool.length > 0) return toNutrObj(pickRaw(pool));
      const fb = foodDBEntries.filter(e => e[0].includes(spRep));
      if (fb.length > 0) return toNutrObj(pickRaw(fb));
      return null;
    }
  }

  // 表記揺れ正規化
  const q = applyFoodAliases(name);

  // ダイレクトマッピング（あいまい検索を避ける）
  const directKey = FOODDB_DIRECT[q];
  if (directKey) {
    const direct = foodDBEntries.filter(e => e[0].includes(directKey));
    if (direct.length > 0) return toNutrObj(pickRaw(direct));
  }

  // 1. 完全一致
  const exact = foodDBEntries.find(e => e[0] === q);
  if (exact) return toNutrObj(exact);
  // 2. fooddb名がqで始まる（「にんじん 根...」→ 生を優先）
  const startsWith = foodDBEntries.filter(e => e[0].startsWith(q));
  if (startsWith.length > 0) return toNutrObj(pickRaw(startsWith));
  // 3. 括弧除去した先頭トークンにqが含まれる（「（まぐろ類）」→まぐろ）
  const tok1 = foodDBEntries.filter(e => {
    const tok = e[0].split(' ')[0].replace(/[（(）)［\]＜＞]/g, '');
    return tok.length >= 2 && tok.includes(q);
  });
  if (tok1.length > 0) return toNutrObj(pickRaw(tok1));
  // 4. 単語境界マッチ（「すいか」の中の「いか」を除外）
  const wordMatch = foodDBEntries.filter(e => wordIncludes(e[0], q));
  if (wordMatch.length > 0) return toNutrObj(pickRaw(wordMatch));
  return null;
}

